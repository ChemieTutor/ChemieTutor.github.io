Formation in transformers
From all the fault gases formed in power transformers, hydrogen is the most common and is generated under most fault conditions; thus, formation of hydrogen is an early indication of serious problems in the transformer's life cycle.[96]

Applications
Consumption in processes
Large quantities of H
2 are needed in the petroleum and chemical industries. The largest application of H
2 is for the processing ("upgrading") of fossil fuels, and in the production of ammonia. The key consumers of H
2 in the petrochemical plant include hydrodealkylation, hydrodesulfurization, and hydrocracking. H
2 has several other important uses. H
2 is used as a hydrogenating agent, particularly in increasing the level of saturation of unsaturated fats and oils (found in items such as margarine), and in the production of methanol. It is similarly the source of hydrogen in the manufacture of hydrochloric acid. H
2 is also used as a reducing agent of metallic ores.[97]

Hydrogen is highly soluble in many rare earth and transition metals[98] and is soluble in both nanocrystalline and amorphous metals.[99] Hydrogen solubility in metals is influenced by local distortions or impurities in the crystal lattice.[100] These properties may be useful when hydrogen is purified by passage through hot palladium disks, but the gas's high solubility is a metallurgical problem, contributing to the embrittlement of many metals,[11] complicating the design of pipelines and storage tanks.[12]

Apart from its use as a reactant, H
2 has wide applications in physics and engineering. It is used as a shielding gas in welding methods such as atomic hydrogen welding.[101][102] H2 is used as the rotor coolant in electrical generators at power stations, because it has the highest thermal conductivity of any gas. Liquid H2 is used in cryogenic research, including superconductivity studies.[103] Because H
2 is lighter than air, having a little more than ​1⁄14 of the density of air, it was once widely used as a lifting gas in balloons and airships.[104]

In more recent applications, hydrogen is used pure or mixed with nitrogen (sometimes called forming gas) as a tracer gas for minute leak detection. Applications can be found in the automotive, chemical, power generation, aerospace, and telecommunications industries.[105] Hydrogen is an authorized food additive (E 949) that allows food package leak testing among other anti-oxidizing properties.[106]

Hydrogen's rarer isotopes also each have specific applications. Deuterium (hydrogen-2) is used in nuclear fission applications as a moderator to slow neutrons, and in nuclear fusion reactions.[5] Deuterium compounds have applications in chemistry and biology in studies of reaction isotope effects.[107] Tritium (hydrogen-3), produced in nuclear reactors, is used in the production of hydrogen bombs,[108] as an isotopic label in the biosciences,[57] and as a radiation source in luminous paints.[109]

The triple point temperature of equilibrium hydrogen is a defining fixed point on the ITS-90 temperature scale at 13.8033 Kelvin.[110]

Coolant
Main article: Hydrogen-cooled turbo generator
Hydrogen is commonly used in power stations as a coolant in generators due to a number of favorable properties that are a direct result of its light diatomic molecules. These include low density, low viscosity, and the highest specific heat and thermal conductivity of all gases.

Energy carrier
See also: Hydrogen economy, Hydrogen infrastructure, and Hydrogen fuel
Hydrogen is not an energy resource,[111] except in the hypothetical context of commercial nuclear fusion power plants using deuterium or tritium, a technology presently far from development.[112] The Sun's energy comes from nuclear fusion of hydrogen, but this process is difficult to achieve controllably on Earth.[113] Elemental hydrogen from solar, biological, or electrical sources requires more energy to make than is obtained by burning it, so in these cases hydrogen functions as an energy carrier, like a battery. Hydrogen may be obtained from fossil sources (such as methane), but these sources are unsustainable.[111]

The energy density per unit volume of both liquid hydrogen and compressed hydrogen gas at any practicable pressure is significantly less than that of traditional fuel sources, although the energy density per unit fuel mass is higher.[111] Nevertheless, elemental hydrogen has been widely discussed in the context of energy, as a possible future carrier of energy on an economy-wide scale.[114] For example, CO
2 sequestration followed by carbon capture and storage could be conducted at the point of H
2 production from fossil fuels.[115] Hydrogen used in transportation would burn relatively cleanly, with some NOx emissions,[116] but without carbon emissions.[115] However, the infrastructure costs associated with full conversion to a hydrogen economy would be substantial.[117] Fuel cells can convert hydrogen and oxygen directly to electricity more efficiently than internal combustion engines.[118]

Semiconductor industry
Hydrogen is employed to saturate broken ("dangling") bonds of amorphous silicon and amorphous carbon that helps stabilizing material properties.[119] It is also a potential electron donor in various oxide materials, including ZnO,[120][121] SnO2, CdO, MgO,[122] ZrO2, HfO2, La2O3, Y2O3, TiO2, SrTiO3, LaAlO3, SiO2, Al2O3, ZrSiO4, HfSiO4, and SrZrO3.[123]

Potential therapeutic claims
Molecular hydrogen dissolved in water has been marketed[124] as a therapeutic product for a wide variety of illnesses,[125] but there is no scientific consensus that hydrogen water has any health benefits in humans.[126]

Biological reactions
Further information: Biohydrogen and Biological hydrogen production (Algae)
H2 is a product of some types of anaerobic metabolism and is produced by several microorganisms, usually via reactions catalyzed by iron- or nickel-containing enzymes called hydrogenases. These enzymes catalyze the reversible redox reaction between H2 and its component two protons and two electrons. Creation of hydrogen gas occurs in the transfer of reducing equivalents produced during pyruvate fermentation to water.[127] The natural cycle of hydrogen production and consumption by organisms is called the hydrogen cycle.[128]

Water splitting, in which water is decomposed into its component protons, electrons, and oxygen, occurs in the light reactions in all photosynthetic organisms. Some such organisms, including the alga Chlamydomonas reinhardtii and cyanobacteria, have evolved a second step in the dark reactions in which protons and electrons are reduced to form H2 gas by specialized hydrogenases in the chloroplast.[129] Efforts have been undertaken to genetically modify cyanobacterial hydrogenases to efficiently synthesize H2 gas even in the presence of oxygen.[130] Efforts have also been undertaken with genetically modified alga in a bioreactor.[131]

